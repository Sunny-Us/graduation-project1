function checkout(oid,modal,action,fn){
  $.ajax({
    url:'http://127.0.0.1:8081/'+action,
    type:"POST",
    data:{
      method:'query',
      token:$.cookie('key'),
      order_id:oid
    },
    success:function(data){
      if(data.result){
        fn(data.data);
      }
    },
    error:function(data){
      console.log(data);
    }
  });
}
function update(oid,postData,action){
  $.ajax({
    url:'http://127.0.0.1:8081/'+action,
    type:"POST",
    data:{
      method:'update',
      token:$.cookie('key'),
      data:JSON.stringify([{[oid]:postData}])
    },
    success:function(data){
      if(data.result){
        alert('修改成功！');
      }else{
        alert('修改失败!');
      }
    },
    error:function(data){
      console.log(data);
    }
  });
}